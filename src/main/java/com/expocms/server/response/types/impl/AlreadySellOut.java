package com.expocms.server.response.types.impl;

public class AlreadySellOut {
	private Long allMoney;
	private int alreadySell;
	private Long maySellMoney;
	private Long lowMoney;
	private int buyNumber;
	private String productName;
	private Long allEarnings;
	private int predictYearEarnings;
	public Long getAllMoney() {
		return allMoney;
	}
	public void setAllMoney(Long allMoney) {
		this.allMoney = allMoney;
	}
	public int getAlreadySell() {
		return alreadySell;
	}
	public void setAlreadySell(int alreadySell) {
		this.alreadySell = alreadySell;
	}
	public Long getMaySellMoney() {
		return maySellMoney;
	}
	public void setMaySellMoney(Long maySellMoney) {
		this.maySellMoney = maySellMoney;
	}
	public Long getLowMoney() {
		return lowMoney;
	}
	public void setLowMoney(Long lowMoney) {
		this.lowMoney = lowMoney;
	}
	public int getBuyNumber() {
		return buyNumber;
	}
	public void setBuyNumber(int buyNumber) {
		this.buyNumber = buyNumber;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public Long getAllEarnings() {
		return allEarnings;
	}
	public void setAllEarnings(Long allEarnings) {
		this.allEarnings = allEarnings;
	}
	public int getPredictYearEarnings() {
		return predictYearEarnings;
	}
	public void setPredictYearEarnings(int predictYearEarnings) {
		this.predictYearEarnings = predictYearEarnings;
	}
	
}
