package com.expocms.server.response.types.impl;

public class Question {
	
	private String opinionTime;
	private String opinionContent;
	private String opinionResult;

	public String getOpinionTime() {
		return opinionTime;
	}

	public void setOpinionTime(String opinionTime) {
		this.opinionTime = opinionTime;
	}

	public String getOpinionContent() {
		return opinionContent;
	}

	public void setOpinionContent(String opinionContent) {
		this.opinionContent = opinionContent;
	}
	
	public String getOpinionResult() {
		return opinionResult;
	}

	public void setOpinionResult(String opinionResult) {
		this.opinionResult = opinionResult;
	}
	
}
